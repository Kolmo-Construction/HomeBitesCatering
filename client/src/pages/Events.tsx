// ─── Notes tab ─────────────────────────────────────────────────────────────

function NotesTab({ event, clientEmail }: { event: EventRow; clientEmail?: string }) {
  const { toast } = useToast();
  const queryClient = useQueryClient();
  const [notes, setNotes] = useState<string>(event.notes ?? "");

  React.useEffect(() => {
    setNotes(event.notes ?? "");
  }, [event.id, event.notes]);

  const notesMutation = useMutation({
    mutationFn: async (value: string) => {
      const res = await apiRequest("PATCH", `/api/events/${event.id}`, { notes: value });
      return res.json();
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: [`/api/events/${event.id}/full`] });
      toast({ title: "Notes saved" });
    },
    onError: () => {
      toast({ title: "Error", description: "Failed to save notes.", variant: "destructive" });
    },
  });

  const handleBlur = () => {
    if (notes !== (event.notes ?? "")) {
      notesMutation.mutate(notes);
    }
  };

  return (
    <div className="space-y-4">
      <Card>
        <CardHeader className="pb-2">
          <CardTitle className="text-sm font-semibold uppercase tracking-wide flex items-center gap-2">
            <StickyNote className="h-4 w-4" />
            Internal Notes
          </CardTitle>
        </CardHeader>
        <CardContent>
          <Textarea
            value={notes}
            onChange={(e) => setNotes(e.target.value)}
            onBlur={handleBlur}
            placeholder="Anything the team needs to know about this event. Saves on blur."
            rows={8}
          />
        </CardContent>
      </Card>

      <Card>
        <CardHeader className="pb-3">
          <CardTitle className="text-base">Contact Timeline</CardTitle>
          <CardDescription>
            Full communication history for this client across all stages.
          </CardDescription>
        </CardHeader>
        <CardContent>
          {clientEmail ? (
            <ContactTimeline email={clientEmail} />
          ) : (
            <p className="text-sm text-muted-foreground">No client email available for timeline.</p>
          )}
        </CardContent>
      </Card>
    </div>
  );
}

// ─── Checklist tab ─────────────────────────────────────────────────────────

function ChecklistTab({ event }: { event: EventRow }) {
  const { toast } = useToast();
  const completed = new Set(event.completedTasks ?? []);

  const toggleMutation = useMutation({
    mutationFn: async ({ taskId, done }: { taskId: string; done: boolean }) => {
      const res = await apiRequest("PATCH", `/api/events/${event.id}/checklist`, { taskId, done });
      return res.json();
    },
    onMutate: async ({ taskId, done }) => {
      await queryClient.cancelQueries({ queryKey: [`/api/events/${event.id}/full`] });
      const previous = queryClient.getQueryData<FullEventPayload>([`/api/events/${event.id}/full`]);
      if (previous) {
        const nextTasks = done
          ? Array.from(new Set([...(previous.event.completedTasks ?? []), taskId]))
          : (previous.event.completedTasks ?? []).filter((t) => t !== taskId);
        queryClient.setQueryData<FullEventPayload>(
          [`/api/events/${event.id}/full`],
          { ...previous, event: { ...previous.event, completedTasks: nextTasks } }
        );
      }
      return { previous };
    },
    onError: (_err, _vars, ctx) => {
      if (ctx?.previous) {
        queryClient.setQueryData([`/api/events/${event.id}/full`], ctx.previous);
      }
      toast({ title: "Error", description: "Failed to update checklist.", variant: "destructive" });
    },
    onSettled: () => {
      queryClient.invalidateQueries({ queryKey: [`/api/events/${event.id}/full`] });
    },
  });

  return (
    <div className="space-y-4">
      {CHECKLIST.map((stage) => {
        const stageIds = stage.items.map((i) => i.id);
        const doneCount = stageIds.filter((id) => completed.has(id)).length;
        const pct = Math.round((doneCount / stageIds.length) * 100);
        return (
          <Card key={stage.key}>
            <CardHeader className="pb-2">
              <div className="flex items-center justify-between">
                <div>
                  <CardTitle className="text-base font-semibold flex items-center gap-2">
                    <ClipboardCheck className="h-4 w-4" />
                    {stage.title}
                  </CardTitle>
                  <p className="text-xs text-muted-foreground mt-0.5">{stage.description}</p>
                </div>
                <div className="text-sm font-semibold text-muted-foreground">
                  {doneCount} / {stageIds.length}
                </div>
              </div>
              <Progress value={pct} className="h-1.5 mt-2" />
            </CardHeader>
            <CardContent className="pt-0">
              <ul className="space-y-2">
                {stage.items.map((it) => {
                  const isDone = completed.has(it.id);
                  return (
                    <li key={it.id} className="flex items-center gap-2">
                      <Checkbox
                        checked={isDone}
                        onCheckedChange={(v) =>
                          toggleMutation.mutate({ taskId: it.id, done: !!v })
                        }
                        id={`task-${it.id}`}
                      />
                      <label
                        htmlFor={`task-${it.id}`}
                        className={`text-sm cursor-pointer ${
                          isDone ? "line-through text-muted-foreground" : ""
                        }`}
                      >
                        {it.label}
                      </label>
                    </li>
                  );
                })}
              </ul>
            </CardContent>
          </Card>
        );
      })}
    </div>
  );
}
