// ─── Helpers ────────────────────────────────────────────────────────────────

function formatDate(iso: string | null): string {
  if (!iso) return "--";
  return new Date(iso).toLocaleDateString("en-US", {
    weekday: "short",
    month: "short",
    day: "numeric",
    year: "numeric",
  });
}

function formatTime(iso: string | null): string {
  if (!iso) return "";
  return new Date(iso).toLocaleTimeString("en-US", {
    hour: "numeric",
    minute: "2-digit",
  });
}

function daysUntil(iso: string): number {
  const now = new Date();
  const then = new Date(iso);
  now.setHours(0, 0, 0, 0);
  then.setHours(0, 0, 0, 0);
  return Math.round((then.getTime() - now.getTime()) / 86400000);
}

function countdownLabel(days: number): string {
  if (days < 0) return `${Math.abs(days)}d ago`;
  if (days === 0) return "Today";
  if (days === 1) return "Tomorrow";
  if (days < 7) return `In ${days}d`;
  if (days < 30) return `In ${Math.ceil(days / 7)}w`;
  return `In ${Math.ceil(days / 30)}mo`;
}

function urgencyBorder(days: number, status: string): string {
  if (status === "completed") return "border-gray-200";
  if (status === "cancelled") return "border-gray-200 opacity-60";
  if (days < 0) return "border-gray-300";
  if (days <= 2) return "border-red-300 bg-red-50/30";
  if (days <= 7) return "border-yellow-300 bg-yellow-50/30";
  return "border-green-200 bg-green-50/20";
}

const STATUS_COLORS: Record<string, string> = {
  confirmed: "bg-green-100 text-green-700 border-green-300",
  "in_progress": "bg-blue-100 text-blue-700 border-blue-300",
  "in-progress": "bg-blue-100 text-blue-700 border-blue-300",
  completed: "bg-gray-100 text-gray-700 border-gray-300",
  cancelled: "bg-red-100 text-red-700 border-red-300",
};

function formatCents(cents: number | null | undefined): string {
  if (cents == null) return "--";
  return `$${(cents / 100).toLocaleString(undefined, { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
}

function titleCase(s: string | null | undefined): string {
  if (!s) return "";
  return s
    .split(/[_\s]+/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase())
    .join(" ");
}

