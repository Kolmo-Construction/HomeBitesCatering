// ─── Prep schedule tab ─────────────────────────────────────────────────────

interface ShoppingRecipe {
  recipeId: number;
  recipeName: string;
  scaledBy: number;
  laborHoursPerBatch: number;
}

interface ShoppingListPayload {
  totalLaborHours: number;
  totalLaborCost: number;
  resolvedRecipes: ShoppingRecipe[];
}

function PrepScheduleTab({ eventId, event }: { eventId: number; event: EventRow }) {
  const canViewFinancials = useCanViewFinancials();
  const { data, isLoading } = useQuery<ShoppingListPayload>({
    queryKey: [`/api/quotes/events/${eventId}/shopping-list`],
  });

  if (isLoading) {
    return (
      <div className="flex items-center justify-center py-12">
        <Loader2 className="h-6 w-6 animate-spin text-muted-foreground" />
      </div>
    );
  }

  if (!data || data.resolvedRecipes.length === 0) {
    return (
      <Card>
        <CardContent className="py-8 text-center text-muted-foreground">
          <ChefHat className="h-8 w-8 mx-auto mb-2 opacity-40" />
          <p>No recipes linked — the shopping list has no resolved recipes yet.</p>
        </CardContent>
      </Card>
    );
  }

  // Sort recipes by labor hours descending so the longest prep starts earliest.
  const recipes = [...data.resolvedRecipes].sort((a, b) => {
    const aTotal = a.laborHoursPerBatch * Math.max(1, a.scaledBy);
    const bTotal = b.laborHoursPerBatch * Math.max(1, b.scaledBy);
    return bTotal - aTotal;
  });

  // "Start by" time = event start minus cumulative prep hours (longest first).
  const eventStart = new Date(event.startTime);

  return (
    <div className="space-y-4">
      <Card>
        <CardContent className="p-4 flex flex-wrap items-center gap-4 text-sm">
          <div>
            <p className="text-muted-foreground">Total prep</p>
            <p className="text-xl font-semibold">
              {data.totalLaborHours.toFixed(1)}h
            </p>
          </div>
          {canViewFinancials && data.totalLaborCost != null && (
            <div>
              <p className="text-muted-foreground">Labor cost</p>
              <p className="text-xl font-semibold text-emerald-700">
                {formatCents(data.totalLaborCost)}
              </p>
            </div>
          )}
          <div className="ml-auto text-xs text-muted-foreground max-w-xs text-right">
            v1: backwards-scheduled from event start, longest jobs first. Mike can override
            ordering mentally — a smart scheduler with dependencies is on the roadmap.
          </div>
        </CardContent>
      </Card>

      <Card>
        <CardContent className="p-0">
          <table className="w-full text-sm">
            <thead className="bg-gray-50 text-xs uppercase text-muted-foreground">
              <tr>
                <th className="text-left px-4 py-2">Recipe</th>
                <th className="text-right px-4 py-2">Batches</th>
                <th className="text-right px-4 py-2">Hrs / batch</th>
                <th className="text-right px-4 py-2">Total hrs</th>
                <th className="text-right px-4 py-2">Start by</th>
              </tr>
            </thead>
            <tbody className="divide-y">
              {recipes.map((r) => {
                const batches = Math.max(1, r.scaledBy);
                const totalHours = r.laborHoursPerBatch * batches;
                const startBy = new Date(
                  eventStart.getTime() - totalHours * 60 * 60 * 1000
                );
                return (
                  <tr key={r.recipeId} className="hover:bg-gray-50">
                    <td className="px-4 py-2 font-medium">{r.recipeName}</td>
                    <td className="px-4 py-2 text-right">{batches}</td>
                    <td className="px-4 py-2 text-right text-muted-foreground">
                      {r.laborHoursPerBatch.toFixed(1)}
                    </td>
                    <td className="px-4 py-2 text-right font-semibold">
                      {totalHours.toFixed(1)}
                    </td>
                    <td className="px-4 py-2 text-right text-muted-foreground">
                      {startBy.toLocaleDateString("en-US", {
                        month: "short",
                        day: "numeric",
                      })}{" "}
                      {startBy.toLocaleTimeString("en-US", {
                        hour: "numeric",
                        minute: "2-digit",
                      })}
                    </td>
                  </tr>
                );
              })}
            </tbody>
          </table>
        </CardContent>
      </Card>
    </div>
  );
}

